<?php

//Arithmetic Operators


$a = 15;
$b = 4;

echo $a + $b;   // 19
echo $a - $b;   // 11
echo $a * $b;   // 60
echo $a / $b;   // 3.75
echo $a % $b;   // 3 (remainder)
echo $a ** $b;  // 50625 (power)

// Extra examples
echo 10 + 20 - 5;      // 25
echo (5 + 3) * 2;      // 16
echo 20 / 5 + 3;       // 7
echo (15 % 4) + 10;    // 13
echo 2 ** 3 ** 2;      // 512 (power is right-associative)
?>
